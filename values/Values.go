package values

import "fmt"

func Values() {

	// String
	fmt.Println("Day la" + " " + "string!")

	// Int
	fmt.Println(2 + 5)
	fmt.Println(7 / 3)

	// Float
	fmt.Println(7.0 / 3.0)

	// Bool
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)

	// output

	//Day la string!
	//7
	//2
	//2.3333333333333335
	//false
	//true
	//false
}
